package com.assistedproject;

public class StringAsInputAndConvertInToPrimitivedataTypes {

	public static void main(String[] args) {
				
				//converting strings to other Datatypes
				
				//converting to char
				//taking h='S' as string input

				char h='S';
				System.out.println("Value of h is : "+h);
				
				//converting to long
				long k=h;
				System.out.println("Value of k is : "+k);
				
				//converting to float
				float j=h;
				System.out.println("Value of j is : "+j);
				
				//converting to integer
				int i=h;
				System.out.println("Value of i is : "+i);				
				//converting to double
				
				double l=h;
				System.out.println("Value of l is : "+l);
				
						
				System.out.println("\n");
				
				
				float w=98.67f;
				//converting float to integer
				int z=(int)w;
				System.out.println("Value of w is : "+w);
				System.out.println("Value of z is : "+z);


			}
	}


